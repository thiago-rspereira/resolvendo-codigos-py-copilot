# Vamos solicitar como entrada dois números e depois vamos realizar uma operação simples entre eles.

# Solicita uma string e um número inteiro como entrada
texto = input("Digite a string que deseja repetir: ")
repeticoes = int(input("Digite o número de repetições desejado: "))

# Repete a string o número de vezes informado
texto_repetido = texto * repeticoes

# Imprime a string repetida
print("Texto repetido:", texto_repetido)